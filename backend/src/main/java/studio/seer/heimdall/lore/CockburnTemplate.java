package studio.seer.heimdall.lore;

/**
 * Скелет scenario_md по весу Коберна (ADR-LORE-027 §5): при пустом scenario_md
 * сервер вставляет шаблон ВЫБРАННОГО веса — casual короткий, fully-dressed полный.
 * Заголовки совпадают с конвенцией §1, которую матчит {@link UcQuality}, так что
 * свежесозданный UC сразу «знает», что заполнять, а линтер — что проверять.
 */
final class CockburnTemplate {

    private CockburnTemplate() {}

    // Шаблон и есть ПРИМЕР: секции заполнены настоящим текстом, который автор
    // заменяет своим.
    //
    // Прежняя редакция ставила подсказки курсивом («_Что запускает сценарий._»)
    // и «1. …» вместо шагов. После PL-40, где линтер стал проверять СОДЕРЖАНИЕ,
    // а не наличие заголовка, такой шаблон перестал проходить собственные
    // проверки: свежесозданный UC открывался с красным чек-листом, хотя ничего
    // ещё не написано и не испорчено. Курсив в корпусе — заполнитель по
    // определению, поэтому шаблон-подсказка и шаблон-заготовка несовместимы.
    //
    // Текст обязан совпадать по смыслу с фронтовым (UsFormModal): один и тот же
    // скелет вставляют и сервер (при пустом scenario_md), и кнопка формы —
    // разойдись они, автор получал бы разную заготовку в зависимости от того,
    // как завёл сценарий.
    static final String CASUAL = String.join("\n",
        "### Триггер",
        "Агент завершил задачу и открыл PR в `develop`.",
        "",
        "### Основной сценарий",
        "1. Агент запрашивает статус CI по head-коммиту PR",
        "2. Все обязательные проверки зелёные — агент вызывает merge",
        "",
        "### Минимальные гарантии",
        "`develop` не получает кода с красным CI ни при каком исходе.",
        "");

    static final String FULLY_DRESSED = String.join("\n",
        "### Триггер",
        "Агент завершил задачу и открыл PR в `develop`.",
        "",
        "### Предусловия",
        "Ветка запушена, CI запущен, у агента есть доступ к Forgejo.",
        "",
        "### Основной сценарий",
        "1. Агент запрашивает статус CI по head-коммиту PR",
        "2. Все обязательные проверки зелёные",
        "3. Агент вызывает merge и получает подтверждение",
        "",
        "### Расширения",
        "2a. Проверка красная или ещё бежит — merge отклоняется с 409, ветка остаётся открытой.",
        "2b. Прогонов нет вовсе — считается не-зелёным: «нет прогонов» это не «всё хорошо».",
        "",
        "### Вариации",
        "squash-merge вместо merge-commit — по настройке репозитория.",
        "",
        "### Минимальные гарантии",
        "`develop` не получает кода с красным CI ни при каком исходе.",
        "",
        "### Гарантии успеха",
        "PR влит, создана связь с релизом, спринт привязан к тому же релизу.",
        "");

    /** Шаблон под вес; вес по умолчанию — из уровня цели (ADR-027-D1). */
    static String forRigor(String rigor) {
        return "casual".equals(rigor) ? CASUAL : FULLY_DRESSED;
    }
}
