package studio.seer.heimdall.lore;

/**
 * Скелет scenario_md по весу Коберна (ADR-LORE-027 §5): при пустом scenario_md
 * сервер вставляет шаблон ВЫБРАННОГО веса — casual короткий, fully-dressed полный.
 * Заголовки совпадают с конвенцией §1, которую матчит {@link UcQuality}, так что
 * свежесозданный UC сразу «знает», что заполнять, а линтер — что проверять.
 */
final class CockburnTemplate {

    private CockburnTemplate() {}

    static final String CASUAL = String.join("\n",
        "### Триггер",
        "_Что запускает сценарий._",
        "",
        "### Основной сценарий",
        "1. …",
        "2. …",
        "",
        "### Минимальные гарантии",
        "_Что истинно даже при неудаче._",
        "");

    static final String FULLY_DRESSED = String.join("\n",
        "### Триггер",
        "_Что запускает сценарий._",
        "",
        "### Предусловия",
        "_Что должно быть истинно до старта._",
        "",
        "### Основной сценарий",
        "1. …",
        "2. …",
        "",
        "### Расширения",
        "_Ветвления вида «2a. …» — номер ссылается на шаг основного сценария._",
        "",
        "### Вариации",
        "_Технологические/данные-вариации (опционально)._",
        "",
        "### Минимальные гарантии",
        "_Что истинно даже при неудаче._",
        "",
        "### Гарантии успеха",
        "_Измеримый результат при успехе._",
        "");

    /** Шаблон под вес; вес по умолчанию — из уровня цели (ADR-027-D1). */
    static String forRigor(String rigor) {
        return "casual".equals(rigor) ? CASUAL : FULLY_DRESSED;
    }
}
