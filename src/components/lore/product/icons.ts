/**
 * Слаг по значению словаря; пусто — если значения нет или оно незнакомо.
 *
 * Возвращаем именно пустую строку, а не иконку-заглушку: незнакомое значение
 * лучше показать одной подписью, чем выдать чужим значком за известное.
 */
export const iconOf = (map: Record<string, string>, value: string | null | undefined): string =>
  map[(value ?? '').toLowerCase()] ?? '';
