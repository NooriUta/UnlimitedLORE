// Иконки продуктового слоя — из подключённого набора game-icons (PL-39).
//
// Раньше уровни цели, типы ценностей и роли рисовались ЭМОДЗИ прямо в коде
// (☁ 🪁 🌊 🐟 · 🔴 🟢 🎯 · 🧑 🤖 ⚙). Эмодзи рисует шрифт системы: на разных
// машинах и в разных темах они выглядят по-разному, не подчиняются цветовым
// токенам и не масштабируются вместе с текстом. Иконка набора — вектор,
// красится `currentColor` и живёт по тем же правилам, что остальной интерфейс.
//
// Владелец на приёмке: «иконки стандартные из справочника, а не кастомные».
// Статусы уже читаются из `KnowDictEntry` (`resolveStatusMeta`); здесь — то,
// у чего словарной записи нет: шкала Кокберна, тип ценности, вид актора.
//
// Слаги сверены с `@iconify-json/game-icons` — несуществующий отрисуется
// пустотой, и подпись рядом останется единственным носителем смысла.

