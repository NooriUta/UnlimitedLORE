import { describe, expect, it } from 'vitest';
import ruLocale from './locales/ru/common.json';
import enLocale from './locales/en/common.json';

/**
 * I18N-03: проверка локализации считает ключи В КОДЕ, а не ключи В ФАЙЛАХ.
 *
 * Аудит I18N-02 закрылся с формулировкой «1368 ключей в обеих локалях, 0
 * непереведённых» — и не заметил, что весь экран сквозного поиска жил на
 * inline-русских дефолтах: неймспейса `lore.search` не было НИ В ОДНОЙ локали.
 *
 * Причина в направлении сверки. I18N-02 сравнивал файлы локалей ДРУГ С ДРУГОМ:
 * русский против английского. Ключ, которого нет ни в одном файле, такая
 * проверка не видит ПО ПОСТРОЕНИЮ — он просто не участвует в сравнении. Обе
 * стороны симметричны, вывод «0 непереведённых» формально верен и при этом
 * ничего не говорит о том, переведён ли интерфейс.
 *
 * `i18next` этому помогает молчанием: второй аргумент `t()` — законный дефолт,
 * отсутствие ключа не ошибка, а штатный путь. Экран выглядит рабочим на любом
 * языке — просто на одном и том же. Первый прогон этой проверки нашёл 358
 * таких ключей: четверть интерфейса была одноязычной при «зелёном» аудите.
 *
 * Поэтому источником истины здесь служит КОД: каждый ключ, который он
 * запрашивает, обязан существовать в каждой локали. Наличие дефолта не
 * оправдание — именно дефолт и прячет пропажу.
 *
 * Файлы читаются через `import.meta.glob`, а не через `node:fs`: проверке
 * хватает возможностей сборщика, а `@types/node` ради неё открыл бы
 * клиентскому коду типы файловой системы — импорт `fs` в браузерный модуль
 * тогда упал бы только в рантайме, вместо ошибки компиляции.
 */

const sources = import.meta.glob('../**/*.{ts,tsx}', { query: '?raw', import: 'default', eager: true }) as Record<string, string>;

/**
 * Ключи вида `namespace.key` в вызовах `t('…')` / `t("…")`.
 *
 * Отрицательный просмотр назад отсекает чужие функции, чьё имя кончается на
 * «t»: `split(`, `at(`, `format(`, `parseInt(`. Без него в выборку попадают
 * разделители и произвольные строковые литералы.
 *
 * Требование точки в ключе — не косметика: односимвольные и бесточечные строки
 * почти всегда аргументы чего-то другого, а все реальные ключи корпуса
 * разделены пространством имён.
 *
 * Чего проверка НЕ видит: ключи, собранные во время работы —
 * `t(`status.${code}`)`. Их существование обязан удерживать тест того экрана,
 * который их строит.
 */
const KEY_RE = /(?<![A-Za-z0-9_$.])t\(\s*['"]([a-zA-Z][a-zA-Z0-9_]*(?:\.[a-zA-Z0-9_-]+)+)['"]/g;

/** Ключ → файл, где он впервые встретился: отчёт о падении должен вести к строке. */
function keysUsedInCode(): Map<string, string> {
  const found = new Map<string, string>();
  for (const [file, text] of Object.entries(sources)) {
    if (/\.test\.tsx?$/.test(file)) continue;
    for (const m of text.matchAll(KEY_RE)) {
      if (!found.has(m[1])) found.set(m[1], file.replace(/^\.\.\//, 'src/'));
    }
  }
  return found;
}

function flatten(obj: Record<string, unknown>, prefix = ''): string[] {
  return Object.entries(obj).flatMap(([k, v]) => {
    const key = prefix ? `${prefix}.${k}` : k;
    return v && typeof v === 'object' ? flatten(v as Record<string, unknown>, key) : [key];
  });
}

const LOCALES = {
  ru: new Set(flatten(ruLocale as Record<string, unknown>)),
  en: new Set(flatten(enLocale as Record<string, unknown>)),
};

describe('i18n · покрытие кода локалями', () => {
  const used = keysUsedInCode();

  it('код вообще просматривается — иначе проверка «зелёная» на пустой выборке', () => {
    // Страховка от тихой поломки регулярки или glob: если извлекатель перестанет
    // находить ключи, все проверки ниже пройдут, не проверив ничего.
    expect(used.size).toBeGreaterThan(1000);
  });

  it.each(Object.keys(LOCALES) as (keyof typeof LOCALES)[])(
    'каждый запрошенный кодом ключ существует в локали %s',
    locale => {
      const present = LOCALES[locale];
      const missing = [...used.keys()].filter(k => !present.has(k)).sort();

      expect(
        missing,
        missing.length
          ? `${missing.length} ключей запрашиваются кодом, но отсутствуют в локали ${locale}.\n`
            + 'Inline-дефолт вторым аргументом t() НЕ считается переводом: он и прячет пропажу.\n'
            + missing.map(k => `  ${k}\n      ← ${used.get(k)}`).join('\n')
          : '',
      ).toEqual([]);
    },
  );

  /**
   * Симметрия локалей — прежняя проверка I18N-02. Она осталась полезной (ключ,
   * забытый в одном файле, — реальная дыра), но теперь она вторая, а не
   * единственная: сама по себе она пропускала целые экраны.
   */
  it('локали симметричны между собой', () => {
    expect([...LOCALES.ru].filter(k => !LOCALES.en.has(k)).sort(), 'есть в ru, нет в en').toEqual([]);
    expect([...LOCALES.en].filter(k => !LOCALES.ru.has(k)).sort(), 'есть в en, нет в ru').toEqual([]);
  });
});
