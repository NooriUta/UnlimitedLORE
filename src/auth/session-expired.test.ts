import { describe, expect, it, vi } from 'vitest';
import { sessionExpired, subscribe, getCurrentUser, AUTH_ENABLED } from './session';

// Протухшая сессия показывалась как «данных нет».
//
// Симптом: список пуст, при этом тот же слайс с токеном отдаёт 25 КБ. Цепочка
// такая — `automaticSilentRenew` обрывается молча, клиент считает себя вошедшим,
// `authHeaders()` не находит валидного токена и шлёт запрос БЕЗ заголовка,
// бэкенд отвечает 401, а экран рендерит пустоту. Диагностика уходит в бэкенд и
// CORS, то есть туда, где всё исправно (поймано владельцем 2026-07-20).
//
// Тест пинует не саму отправку на вход — это делает AuthGate, — а СИГНАЛ,
// который его запускает. Пропадёт сигнал — вернётся пустой экран, и разница
// снова будет незаметна.

describe('sessionExpired', () => {
  it('оповещает подписчиков — иначе AuthGate не узнает и экран останется пустым', () => {
    const seen = vi.fn();
    const off = subscribe(seen);
    sessionExpired();
    off();

    // При выключенном auth сессии нет вовсе, и сбрасывать нечего — молчание
    // здесь правильное поведение, а не пропущенный сигнал.
    if (AUTH_ENABLED) {
      expect(seen).toHaveBeenCalled();
    } else {
      expect(seen).not.toHaveBeenCalled();
    }
  });

  it('после сброса пользователя нет — состояние согласовано с тем, что видит бэкенд', () => {
    sessionExpired();
    expect(getCurrentUser()).toBeNull();
  });

  it('повторный вызов не оповещает второй раз — 401 приходит пачкой на каждый слайс', () => {
    // Экран тянет несколько слайсов разом, и все получают 401 одновременно.
    // Без защёлки это дало бы залп перерисовок и, что хуже, гонку редиректов.
    sessionExpired();
    const seen = vi.fn();
    const off = subscribe(seen);
    sessionExpired();
    off();
    expect(seen).not.toHaveBeenCalled();
  });
});
